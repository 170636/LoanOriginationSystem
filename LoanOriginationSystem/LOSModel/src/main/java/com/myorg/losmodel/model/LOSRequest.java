package com.myorg.losmodel.model;

public class LOSRequest {
	
	private String userId;
	private String questionBank;
	private String mortgageApplicationId;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getQuestionBank() {
		return questionBank;
	}
	public void setQuestionBank(String questionBank) {
		this.questionBank = questionBank;
	}
	public String getMortgageApplicationId() {
		return mortgageApplicationId;
	}
	public void setMortgageApplicationId(String mortgageApplicationId) {
		this.mortgageApplicationId = mortgageApplicationId;
	}
	
	

}
